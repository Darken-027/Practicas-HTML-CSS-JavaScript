// Verla estructura de los operadores de asignacion, incremento y decremento
let a = 1;

document.write('valor de a: ', a);
document.write('<br>');
a++; // este operdor aumenta el valor en 1
document.write('valor de a: ', a);
document.write('<br>');
a+=5; // aumenta la variable dependiendo del valor que se asigne incremento
document.write('valor de a: ', a);
document.write('<br>');
a--; //resta valor en uno a la variable
document.write('valor de a: ', a);
document.write('<br>');
a-=3; //resta valor a la variable dependiendo del valor que se asigne decremento
document.write('valor de a: ', a);
document.write('<br>');
a*=2; //multiplica a
document.write('valor de a: ', a); 
document.write('<br>');
a/=3; // divide a
document.write('valor de a: ', a);
document.write('<br>');
a**=2;
document.write('valor de a: ', a);
document.write('<br>');


